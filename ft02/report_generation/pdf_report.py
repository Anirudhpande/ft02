def _safe_text(text):
    """Sanitize text for ReportLab - replace problematic Unicode chars."""
    if not isinstance(text, str):
        text = str(text)
    replacements = {
        '\u20b9': 'Rs.',
        '\u26a0': '[!]',
        '\u2014': '-',
        '\u2013': '-',
        '\u2018': "'",
        '\u2019': "'",
        '\u201c': '"',
        '\u201d': '"',
        '\u2022': '*',
    }
    for char, repl in replacements.items():
        text = text.replace(char, repl)
    text = text.encode('latin-1', errors='replace').decode('latin-1')
    return text


def _get_styles():
    """Create custom report styles."""
    styles = getSampleStyleSheet()

    styles.add(ParagraphStyle(
        name="ReportTitle",
        parent=styles["Title"],
        fontSize=22,
        textColor=colors.HexColor("#1a237e"),
        spaceAfter=6,
        alignment=TA_CENTER,
    ))
    styles.add(ParagraphStyle(
        name="SectionHeader",
        parent=styles["Heading2"],
        fontSize=14,
        textColor=colors.HexColor("#1565C0"),
        spaceBefore=16,
        spaceAfter=8,
        borderWidth=1,
        borderColor=colors.HexColor("#1565C0"),
        borderPadding=4,
    ))
    styles.add(ParagraphStyle(
        name="SubHeader",
        parent=styles["Heading3"],
        fontSize=11,
        textColor=colors.HexColor("#333333"),
        spaceBefore=8,
        spaceAfter=4,
    ))
    styles.add(ParagraphStyle(
        name="BodyText2",
        parent=styles["BodyText"],
        fontSize=10,
        leading=14,
        alignment=TA_JUSTIFY,
    ))
    styles.add(ParagraphStyle(
        name="AlertRed",
        parent=styles["BodyText"],
        fontSize=10,
        textColor=colors.HexColor("#D32F2F"),
        fontName="Helvetica-Bold",
    ))
    styles.add(ParagraphStyle(
        name="AlertGreen",
        parent=styles["BodyText"],
        fontSize=10,
        textColor=colors.HexColor("#388E3C"),
        fontName="Helvetica-Bold",
    ))
    styles.add(ParagraphStyle(
        name="AlertOrange",
        parent=styles["BodyText"],
        fontSize=10,
        textColor=colors.HexColor("#F57F17"),
        fontName="Helvetica-Bold",
    ))

    return styles


